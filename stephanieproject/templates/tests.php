        <h2>Clinique :</h2>
        <p>Le psychologue clinicien réalise des analyses et des évaluations psychologiques de patients en souffrances morales. Grâce à ce travail de recueil d’informations au travers d’entretiens et de recherches, le psychologue conçoit et met en place des actions préventives et curatives. L’objectif étant d’accompagner des personnes qui le souhaitent sur un chemin de connaissances et de responsabilité. Chaque étape partagée avec le patient est le fruit d’un long travail d’identification des besoins et des attentes.</p>

