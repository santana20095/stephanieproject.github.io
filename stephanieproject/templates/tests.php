        <h2>Test des intelligences multiples :</h2>
        <p>Le test des intelligences multiples a été mis en avant pour démontrer que plusieurs facettes du cerveau représentent l’intelligence. Ainsi, est fait le constat que même si des altérations de certaines capacités intellectuelles sont présentes, d’autres peuvent fonctionner et prendre place.

        Le principe démontre qu’il y a une complémentarité entre elles et que certaines prennent le dessus sur d’autres afin de faciliter l’apprentissage. Elles

        sont donc plus ou moins développées chez chacun et évoluent avec de l’entrainement pour les plus fragiles ou l’utilisation pour les pondérantes.

        Face à des difficultés, il est judicieux de prendre en compte le profil d’intelligence afin de pouvoir adapter les outils d’apprentissages favorisant une meilleure mémorisation, imprégnation et compréhension des notions à apprendre.</p>
