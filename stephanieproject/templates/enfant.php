        <h1>Les évaluations</h1>
        <h2>Test psychométrique – WPPSI IV (2 ans et 6 mois à 7 ans et 7 mois) :</h2>
        <p>Bilan élaboré pour mieux prendre en compte les spécificités du développement de l’enfant.</p>
        <h2>Test psychométrique – WISC V (6 ans à 16 ans et 11 mois) :</h2>
        <p>Bilan explorant les différents processus impliqués dans l’apprentissage. Il permet d’apprécier les capacités de l’enfant et d’identifier les difficultés cognitives.</p>
        <h2>Test psychométrique – NEPSY II (5 ans à 16 ans et 11 mois) :</h2>
        <p>Bilan renseignant sur les troubles typiques de l’enfance et de l’adolescence : lecture, mathématiques, attention/concentration, comportement, retard ou trouble du langage, retards ou troubles perceptifs et/ou moteurs, maturité scolaire et difficultés de sociabilité ou de relations interpersonnelles.</p>
        <h2>Test psychométrique – Vineland II (1 à 90 ans) :</h2>
        <p>Bilan explorant les domaines de la communication, des compétences de la vie quotidienne, la socialisation et, au-delà de 7 ans, la motricité. <br> Pour les enfants, il permet d’évaluer le niveau global d’autonomie et de capacités d’adaptation.</p>
        <h2>Bilan scolaire (primaire et collège) :</h2>
        <p>Prise en charge personnelle visant à réaliser un bilan sur le programme scolaire afin de connaitre les différentes compétences des enfants. Cela permet d’évaluer les points maitrisés et les points à revoir légitimant un programme individuel de remise à niveau.</p>
        <h2>Test des intelligences multiples :</h2>
        <p>Le test des intelligences multiples a été mis en avant pour démontrer que plusieurs facettes du cerveau représentent l’intelligence. Ainsi, est fait le constat que même si des altérations de certaines capacités intellectuelles sont présentes, d’autres peuvent fonctionner et prendre place.
        <br>
        Le principe démontre qu’il y a une complémentarité entre elles et que certaines prennent le dessus sur d’autres afin de faciliter l’apprentissage. Elles sont donc plus ou moins développées chez chacun et évoluent avec de l’entrainement pour les plus fragiles ou l’utilisation pour les pondérantes.
        <br>
        Face à des difficultés, il est judicieux de prendre en compte le profil d’intelligence afin de pouvoir adapter les outils d’apprentissages favorisant une meilleure mémorisation, imprégnation et compréhension des notions à apprendre.</p>

